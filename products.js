// Geo-Location Fetcher
(function() {
    try {
        if (!localStorage.getItem('user_geo')) {
            // Try ipwho.is first
            fetch('https://ipwho.is/')
                .then(res => res.json())
                .then(data => {
                    if (data && data.success) {
                        const geo = {
                            city: data.city || 'Unknown',
                            region: data.region || 'Unknown'
                        };
                        localStorage.setItem('user_geo', JSON.stringify(geo));
                    } else {
                        throw new Error("ipwho.is failed");
                    }
                })
                .catch(err => {
                    console.warn("ipwho.is failed, trying ipapi.co", err);
                    // Fallback 1: ipapi.co
                    fetch('https://ipapi.co/json/')
                        .then(res => res.json())
                        .then(data => {
                            const geo = {
                                city: data.city || 'Unknown',
                                region: data.region || 'Unknown'
                            };
                            localStorage.setItem('user_geo', JSON.stringify(geo));
                        })
                        .catch(err2 => {
                            console.warn("ipapi.co failed, trying freeipapi.com", err2);
                            // Fallback 2: freeipapi.com
                            fetch('https://freeipapi.com/api/json')
                                .then(res => res.json())
                                .then(data => {
                                    const geo = {
                                        city: data.cityName || 'Unknown',
                                        region: data.regionName || 'Unknown'
                                    };
                                    localStorage.setItem('user_geo', JSON.stringify(geo));
                                })
                                .catch(err3 => console.error("All geo APIs failed", err3));
                        });
                });
        }
    } catch(e) {
        console.error("Geo error", e);
    }
})();

// Realtime User Heartbeat
(function() {
    try {
        function updateHeartbeat() {
            let sessionId = sessionStorage.getItem('user_session_id');
            if (!sessionId) {
                sessionId = 'SESS_' + Math.random().toString(36).substring(2, 9) + Date.now();
                sessionStorage.setItem('user_session_id', sessionId);
            }
            
            let pageName = window.location.pathname.split('/').pop() || 'index.html';
            if (pageName.includes('product.html')) {
                const urlParams = new URLSearchParams(window.location.search);
                const pId = urlParams.get('id');
                const pName = (typeof productsData !== 'undefined' && productsData[pId]) ? productsData[pId].name : 'Product Detail';
                const shortName = pName.length > 25 ? pName.substring(0, 25) + '...' : pName;
                pageName = `Product: ${shortName}`;
            } else if (pageName.includes('address.html')) {
                pageName = 'Address Form';
            } else if (pageName.includes('cart.html')) {
                pageName = 'Order Summary';
            } else if (pageName.includes('payment.html')) {
                pageName = 'Payment Screen';
            } else if (pageName.includes('index.html')) {
                pageName = 'Home Page';
            }
            
            const geo = JSON.parse(localStorage.getItem('user_geo') || '{"city":"Unknown","region":"Unknown"}');
            const sessionData = {
                id: sessionId,
                page: pageName,
                last_active: Date.now(),
                device: /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent) ? 'Mobile' : 'Desktop',
                city: geo.city,
                region: geo.region
            };
            localStorage.setItem('admin_sess_' + sessionId, JSON.stringify(sessionData));
        }
        setInterval(updateHeartbeat, 3000);
        updateHeartbeat();
    } catch(e) {
        console.error("Heartbeat error", e);
    }
})();
